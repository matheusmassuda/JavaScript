function somarValores(primeiroValor=0, segundoValor=0) {
    return primeiroValor + segundoValor
}

let primeiroValor = 74
let segundoValor = 16
console.log(`A soma entre ${primeiroValor} e ${segundoValor} é: ${somarValores(primeiroValor, segundoValor)}.`)

//primeiroValor=0 significa que se não for declarado, valerá zero